package com.us.weavx.model;

public class RequestSpring {
	
	private Long id;
	
	

	public RequestSpring() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	@Override
	public String toString() {
		return "RequestSpring [id=" + id + "]";
	}
	
	

}
